#include "Class.h"
#include <iostream>
#include <math.h>


//CONSTRUCTORS
VectorXY::VectorXY(double x, double y) : X(x), Y(y){
	length = sqrt(pow(x, 2) + pow(y, 2));
}
VectorXY::VectorXY() : VectorXY(NULL, NULL){}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------


//METHODS OF VECTOR
void VectorXY::setX(double x){
	X = x;
}

void VectorXY::setY(double y){
	Y = y;
}

void VectorXY::show(){
	std::cout << "(" << X << "," << Y << ")" << std::endl;
}
